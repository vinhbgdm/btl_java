                    if(ArrNhanVien.size()==0){
                        n=100;
                    }else{
                        String a = ArrNhanVien.get(ArrNhanVien.size()-1).getManv();
                        a = a.substring(3);
                        n= Integer.parseInt(a);
                        n++;
                    }
                    String ma = "NVI"+n;
                    NhanVien nv = new NhanVien(ma,heluong, Ca, TextTenNV.getText().trim(), tuoi, 
                    Email, TextDCNV.getText().trim(),duongdananhnv);
                    ArrNhanVien.add(nv);
                    duongdananhnv=null;
                    lbanhnv.setIcon(ResizeImage(anhdaidien, lbanhnv));
                    showdata(ArrNhanVien, modelB);
