    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        int n;
        n = Tablephim.getSelectedRow();
        if(n==-1){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn phim","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else{
            String maph = null;
            try {
                maph = JOptionPane.showInputDialog(this,"Nhập mã phòng cần thêm phim","Nhập mã",
                JOptionPane.DEFAULT_OPTION);
                if(maph.trim().equals("")){
                    JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã phòng","Thông báo",JOptionPane.ERROR_MESSAGE);
                }else{
                    Phong pho = new Phong(maph.trim().toUpperCase());
                if(!Arrphong.contains(pho)){
                    JOptionPane.showMessageDialog(this, "Không tồn tại phòng này","Thông báo",JOptionPane.ERROR_MESSAGE);
                }else{
                    Phim phim = ArrPhim.get(n);
                    int i = Arrphong.indexOf(pho);
                    Arrphong.get(i).Arrphim.add(phim);
                    JOptionPane.showMessageDialog(rootPane, "Đã thêm 1 phim vào phòng "+maph.toUpperCase());
                    showdata(Arrphong, modelA);
                    }
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập phòng","Thông báo",JOptionPane.ERROR_MESSAGE);
            }
        }

        
    }//GEN-LAST:event_jButton17ActionPerformed

